/**
 * 2. Write a java program to print 1 to 20 numbers using the while loop
 */
package LOOPS;

public class PrintingNumbers {


        public static void main(String[] args) {
            int i = 1; // Initialize the counter variable

            // Loop as long as 'i' is less than or equal to 20
            while (i <= 20) {
                System.out.println(i); // Print the current value of 'i'
                i++; // Increment 'i' for the next iteration
            }
        }
    }

