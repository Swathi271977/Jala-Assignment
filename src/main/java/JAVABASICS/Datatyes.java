/**
* 4. Define variables for different Data Types int, Boolean, char, float,
        * double and print on the Console
 */
package JAVABASICS;

public class Datatyes {
    public static void main(String[] args) {

            int     yearOfBirth   = 1995;     // 32‑bit signed integer
            boolean isJavaFun     = true;     // true / false
            char    middleInitial = 'S';      // single 16‑bit Unicode character
            float   piApprox      = 3.14159f; // 32‑bit IEEE‑754 (note trailing ‘f’)
            double  distanceKm    = 384_400;  // 64‑bit IEEE‑754 (Moon‑to‑Earth)

            System.out.println("int     : " + yearOfBirth);
            System.out.println("boolean : " + isJavaFun);
            System.out.println("char    : " + middleInitial);
            System.out.println("float   : " + piApprox);
            System.out.println("double  : " + distanceKm);
        }
    }


